# ── Орц → ангилал (нэгдсэн жагсаалтад) ──
def categorize(n):
    s = n.lower()
    if 'шөлний үндэс' in s:
        return 'Шөлний суурь'
    if 'далайн байцаа' in s or 'wakame' in s or 'тофу' in s:
        return 'Далайн бүтээгдэхүүн'
    if any(k in s for k in ['мах','хонь','хавирга','цорой','гуя','цээж','сийр','яс','хиам']):
        return 'Мах / уураг'
    if 'өнд' in s:
        return 'Өндөг'
    if any(k in s for k in ['сүү','өрөм','цөцгий']):
        return 'Сүүн бүтээгдэхүүн'
    if any(k in s for k in ['oil','тос']):
        return 'Тос'
    toks = s.replace('(',' ').replace('/',' ').split()
    if 'ус' in toks:
        return 'Ус'
    # амтлагч / халуун ногоо / соус (ногооноос ӨМНӨ шалгана)
    if s.startswith('гаа') or s.startswith('сармис') or \
       any(k in s for k in ['давс','чихэр','соус','паст','майонез','цуу','шүүс','карри',
                            'rosemary','тимьян','мускат','кунжут','мөөгөнцөр',
                            'хар чинжүү','цагаан чинжүү','cayenne','чинжүүний бөмбөг',
                            'чулуу','шорон']):
        return 'Амтлагч · соус · бусад'
    if any(k in s for k in ['будаа','гурил','гоймон','пүнтүүз','цардуул']):
        return 'Гурил · будаа · гоймон'
    if any(k in s for k in ['байцаа','төмс','сонгино','лооль','хэмх','чинжүү','манжин',
                            'брокли','ногоо','мөөг','вандуй','налам']):
        return 'Ногоо'
    return 'Бусад'

